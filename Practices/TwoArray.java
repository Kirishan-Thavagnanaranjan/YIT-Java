class TwoArray{
	
	public static void main(String args[]){
		
		int[][] x = new int[5][2];
		
		x[0][0] = 45;
		x[0][1] = 50;
		
		x[1][0] = 43;
		x[1][1] = 35;
		
		x[2][0] = 56;
		x[2][1] = 67;
		
		x[3][0] = 56;
		x[3][1] = 50;
	
		x[4][0] = 46;
		x[4][1] = 88;
		
		System.out.println(x[0][1]);
		System.out.println(x[1][0]);
		System.out.println(x[2][0]);
		System.out.println(x[3][1]);
		System.out.println(x[4][0]);

	}
}
		
		